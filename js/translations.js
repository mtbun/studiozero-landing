// Current language
let currentLanguage = 'en';

// Detect browser language
function detectBrowserLanguage() {
    const browserLang = navigator.language || navigator.userLanguage;
    const lang = browserLang.split('-')[0].toLowerCase();
    
    if (translations[lang]) {
        return lang;
    }
    return 'en';
}

// Get saved language or detect from browser
function getInitialLanguage() {
    const saved = localStorage.getItem('studio_zero_lang');
    if (saved && translations[saved]) {
        return saved;
    }
    return detectBrowserLanguage();
}

// Apply translations to the page
function setLanguage(lang) {
    if (!translations[lang]) {
        lang = 'en';
    }
    
    currentLanguage = lang;
    localStorage.setItem('studio_zero_lang', lang);
    
    // Update HTML lang attribute
    document.documentElement.lang = lang;
    
    // Update all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang][key]) {
            el.textContent = translations[lang][key];
        }
    });
    
    // Update language selector button text
    const langBtn = document.querySelector('.lang-btn-text');
    if (langBtn) {
        const flags = { en: 'ðŸ‡¬ðŸ‡§', tr: 'ðŸ‡¹ðŸ‡·', es: 'ðŸ‡ªðŸ‡¸' };
        const names = { en: 'EN', tr: 'TR', es: 'ES' };
        langBtn.textContent = `${flags[lang]} ${names[lang]}`;
    }
    
    // Close dropdown
    const dropdown = document.querySelector('.lang-dropdown');
    if (dropdown) {
        dropdown.classList.remove('active');
    }
}

// Initialize language system
function initLanguage() {
    const lang = getInitialLanguage();
    setLanguage(lang);
    
    // Language dropdown toggle
    const langToggle = document.querySelector('.lang-toggle');
    const langDropdown = document.querySelector('.lang-dropdown');
    
    if (langToggle && langDropdown) {
        langToggle.addEventListener('click', (e) => {
            e.stopPropagation();
            langDropdown.classList.toggle('active');
        });
        
        // Close dropdown when clicking outside
        document.addEventListener('click', () => {
            langDropdown.classList.remove('active');
        });
        
        // Language option click
        document.querySelectorAll('.lang-option').forEach(option => {
            option.addEventListener('click', (e) => {
                e.stopPropagation();
                const lang = option.getAttribute('data-lang');
                setLanguage(lang);
            });
        });
    }
}

// Export for use in main.js
if (typeof window !== 'undefined') {
    window.initLanguage = initLanguage;
    window.setLanguage = setLanguage;
}
